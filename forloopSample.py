x = 0
for index in range(10):
    #10 means we want the for loop to be executed 10 times 0 to 9
    x += 10
    print("The value of X is {0}".format(x))

# Range with three arguments ( last argument is increment)
y = 0
for index in range(2, 10, 2):
    y += 10
    print("The value of Y is {0}".format(y))
